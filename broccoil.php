                    <h2>Harvesting</h2>
                    <p>
                        Harvest broccoli in the morning, when the buds of the head are firm and tight, just before the
                        heads<br>
                        flower.<br>
                        If you do see yellow petals, harvest immediately, as the quality will decrease rapidly.<br>
                        Cut heads from the plant, taking at least 6 inches of stem. Make a slanted cut on the stalk to
                        allow<br>
                        water to slide away. (Water can pool and rot the center of a flat-cut stalk, runing the
                        secondary<br>
                        heads.)<br>
                        Most varieties have side-shoots that will continue to develop after the main head is harvested.
                        You
                        can<br>
                        harvest from one plant for many weeks, in some cases, from spring to fall, if your summer isn’t
                        too
                        hot<br>
                        <br>
                        <br>
                    </p>

                    <h2>How to plant Broccoli?</h2>

                    <p>
                        If starting seeds outdoors, sow seeds 1/2-inch deep and 3 inches apart.<br>
                        Once seedlings reach a height of& 2 to 3 inches, thin them so that plants are 12 to 20 inches
                        apart.<br>
                        If you started seeds indoors, plant transplants that are 4 to 6 weeks old (and have 4 or 5
                        leaves)<br>
                        outdoors, 12 to 20 inches apart, in holes slightly deeper than their container depth.<br>
                        Space rows of broccoli 3 feet apart. (Closer spacing yields smaller main heads, but more
                        secondary<br>
                        heads.)<br>
                        <img class="image" src="images/brocoli_water.png" data-inViewport="slideLeft" height="280"
                            width="280">
                        Water well at the time of planting<br>
                        <br>
                        <br>
                    </p>

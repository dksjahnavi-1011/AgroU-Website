                    <h2>When to Plant Broccoli?</h2>
                    <p>
                        Broccoli is cool-season crop, so it should be started in early- to mid-spring (depending on
                        your<br>
                        climate) for an early summer crop, or in mid- to late summer for a fall crop. High temperatures
                        will<br>
                        affect development of the broccoli head (the harvestable part), so the goal is to get broccoli
                        to
                        mature<br>
                        before or after high temperatures are expected.<br>
                        Broccoli seeds are capable of germinating in soil temperatures as low as 40°F (4°C), but warmer
                        soil
                        is<br>
                        preferred and will greatly speed up development.<br>
                        For spring plantings, broccoli may be started indoors or outdoors a few weeks ahead of your last
                        spring<br>
                        frost date. Consult our Planting Calendar to see recommended dates for your area. Generally
                        speaking:<br>
                        Start seeds indoors 6 to 8 weeks before your last frost date.<br>
                        Sow seeds outdoors 2 to 3 weeks before your last frost date, or as soon as the soil can be
                        worked in
                        the<br>
                        spring.<br>
                        For fall plantings (best in warm climates), sow seeds outdoors 85 to 100 days before the first
                        fall<br>
                        frost, when soil and ambient temperatures are high.<br>
                        <br>
                        <br>
                    </p>
